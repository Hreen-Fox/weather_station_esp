#ifndef WEB_SERVER_H
#define WEB_SERVER_H

// Подключаем библиотеку веб-сервера для ESP8266
#include <ESP8266WebServer.h>

// Объявляем глобальный объект веб-сервера (реализация будет в .cpp файле)
// Порт 80 - стандартный HTTP порт
extern ESP8266WebServer server;

// Основные функции инициализации и обработки запросов
void initWebServer();        // Инициализация и запуск веб-сервера
void handleWebRequests();    // Обработка входящих веб-запросов (вызывается в loop())

// Обработчики основных HTML-страниц
// Каждая функция отправляет соответствующую статическую HTML-страницу
void handleStatus();         // Главная страница - Статус устройства
void handleAttributes();     // Страница характеристик - все статические данные
void handleSensors();       // Страница управления датчиками
void handleSettings();       // Страница настроек системы

// Обработчики API-эндпоинтов для мобильного приложения и динамических данных
// Все API возвращают данные в формате JSON
void handleApiStatus();              // Текущий статус устройства (память, Wi-Fi, датчики)
void handleApiSensorsList();         // Список всех датчиков с их параметрами
void handleApiSettings();            // Получение/сохранение системных настроек
void handleApiSensorsToggle();       // Включение/выключение датчика (без удаления)
void handleApiSensorsDelete();       // Удаление датчика из системы

#endif